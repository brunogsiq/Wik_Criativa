/*
    O que é uma classe?
        Estrutura que permite agrupar instruções, informações, dados.
        Ex:
            public class Main
            {
                //código, instrução...
            }

        A class Main, poderá possuir um metódo chamado main.
        Ex:
            public class Main
            {
                public static void main(String[] args)
                {
                    //código, instrução...
                }
            }
 */